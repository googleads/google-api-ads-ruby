# This example gets all roles that are defined for the users of the network.

require 'ad_manager_api'

def get_all_roles(ad_manager)
  # Get the UserService.
  user_service = ad_manager.service(:UserService, API_VERSION)

  # Get all user roles.
  roles = user_service.get_all_roles()

  # Display the results.
  roles.each do |role|
    puts 'Role with ID %d and name "%s" was found.' % [role[:id], role[:name]]
  end
end

if __FILE__ == $0
  API_VERSION = :v201808

  # Get AdManagerApi instance and load configuration from ~/ad_manager_api.yml.
  ad_manager = AdManagerApi::Api.new

  # To enable logging of SOAP requests, set the log_level value to 'DEBUG' in
  # the configuration file or provide your own logger:
  # ad_manager.logger = Logger.new('ad_manager_xml.log')

  begin
    get_all_roles(ad_manager)

  # HTTP errors.
  rescue AdsCommon::Errors::HttpError => e
    puts "HTTP Error: %s" % e

  # API errors.
  rescue AdManagerApi::Errors::ApiException => e
    puts "Message: %s" % e.message
    puts 'Errors:'
    e.errors.each_with_index do |error, index|
      puts "\tError [%d]:" % (index + 1)
      error.each do |field, value|
        puts "\t\t%s: %s" % [field, value]
      end
    end
  end
end
